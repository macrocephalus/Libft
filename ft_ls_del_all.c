#include "libft.h"

void					ft_ls_del_all(t_list **lst)
{
	t_list *temp;

	while (*lst)
	{
		temp = *lst;
		ft_ls_del_t(&(*lst));
		*lst = temp->next;
	}
	*lst = NULL;
}
