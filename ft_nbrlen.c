#include "libft.h"

size_t		ft_nbrlen(int n)
{
	unsigned int		i;

	i = 0;
	if (n < 0)
		i++;
	while (n / 10)
	{
		n /= 10;
		i++;
	}
	i++;
	return (i);
}
